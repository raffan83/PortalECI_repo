package it.portalECI.DTO;

public class ProvinciaDTO {
	private int id;
	
	private String sigla_province;
	private String regione_province;
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getSigla_province() {
		return sigla_province;
	}
	public void setSigla_province(String sigla_province) {
		this.sigla_province = sigla_province;
	}
	public String getRegione_province() {
		return regione_province;
	}
	public void setRegione_province(String regione_province) {
		this.regione_province = regione_province;
	}
	
}
